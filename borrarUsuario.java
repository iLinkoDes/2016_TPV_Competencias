    private void llenarLista(){
        
        lista.clear();
        
        mysql.crearConexion(false);
        
        String sql = "SELECT * FROM usuarios";
        
        ResultSet resul = mysql.ejecutarConsulta(sql);
        
        try{
            while(resul.next()){
                lista.addElement(resul.getString("nombre"));
            }
        }catch(Exception e) {
            JOptionPane.showMessageDialog(null, "Error de SQL" + e.getMessage(),
            "Error de SQL", JOptionPane.ERROR_MESSAGE);
        }
        jlUsuarios.setModel(lista);
        
        mysql.cerrarConexion();
    }
    
    public void limpiar(){
        jlNombre.setText("");
        jlNomUsuario.setText("");
        jlID.setText("");
        
        jlUsuarios.removeSelectionInterval(0, jlUsuarios.getMinSelectionIndex());
    }

