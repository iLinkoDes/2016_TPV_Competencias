    private void bBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bBorrarActionPerformed
        if(!jlID.getText().isEmpty()){
            
            mysql.crearConexion(false);
            
            String sql = "DELETE FROM usuarios WHERE id_usuario = " + jlID.getText();
            
            int confirmar = JOptionPane.showConfirmDialog(null, "Â¿Desea eliminar el usuario? No se podra recuperar...",
                    "Eliminar registro", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
            
            if(JOptionPane.YES_OPTION == confirmar){
                mysql.actualizarRegistro(sql);
            }
            mysql.cerrarConexion();
            
            limpiar();
            
            mysql.cerrarConexion();
            
            llenarLista();
        }else{
            JOptionPane.showMessageDialog(null, "Parece que no has seleccionado ningun usuario!",
                    "Atencion", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_bBorrarActionPerformed

    private void jlUsuariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jlUsuariosMouseClicked
        mysql.crearConexion(false);
        
        String sql = "SELECT * FROM usuarios WHERE nombre = '" +
                jlUsuarios.getSelectedValue().toString() + "'";
        
        ResultSet resul = mysql.ejecutarConsulta(sql);
        
        try{
            resul.first();
            jlNombre.setText(resul.getString("nombre"));
            jlNomUsuario.setText(resul.getString("usuario"));
            jlID.setText(resul.getString("id_usuario"));
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error de SQL:" + e.getMessage(),
                    "Error de SQL", JOptionPane.ERROR_MESSAGE);
        }
        mysql.cerrarConexion();
    }//GEN-LAST:event_jlUsuariosMouseClicked

